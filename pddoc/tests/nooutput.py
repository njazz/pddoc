import sys
import StringIO


class NoOutput(object):
    def __init__(self):
        self._cout = sys.stdout
        self._cerr = sys.stderr
        sys.stdout = StringIO.StringIO()
        sys.stderr = StringIO.StringIO()

    def __del__(self):
        sys.stdout = self._cout
        sys.stderr = self._cerr